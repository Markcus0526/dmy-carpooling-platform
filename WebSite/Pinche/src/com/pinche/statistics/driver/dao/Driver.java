package com.pinche.statistics.driver.dao;


public class Driver {
	private int num;
	private int countofDriver;
	private int countofDriverReg;
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public int getCountofDriver() {
		return countofDriver;
	}
	public void setCountofDriver(int countofDriver) {
		this.countofDriver = countofDriver;
	}
	public int getCountofDriverReg() {
		return countofDriverReg;
	}
	public void setCountofDriverReg(int countofDriverReg) {
		this.countofDriverReg = countofDriverReg;
	}
	
}
