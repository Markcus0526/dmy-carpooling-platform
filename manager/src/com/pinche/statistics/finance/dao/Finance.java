package com.pinche.statistics.finance.dao;

public class Finance {
	private int id;
	private String date;
	private int money_all;
	private int money_loan;
	private int money_info;
	private int money_person;
	private int money_group;
	private int money_unit;
	
	public int getId() {
		return id;
	}
	
	public void setId(int id) {
		this.id = id;
	}

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

	public int getMoney_all() {
		return money_all;
	}

	public void setMoney_all(int money_all) {
		this.money_all = money_all;
	}

	public int getMoney_loan() {
		return money_loan;
	}

	public void setMoney_loan(int money_loan) {
		this.money_loan = money_loan;
	}

	public int getMoney_info() {
		return money_info;
	}

	public void setMoney_info(int money_info) {
		this.money_info = money_info;
	}

	public int getMoney_person() {
		return money_person;
	}

	public void setMoney_person(int money_person) {
		this.money_person = money_person;
	}

	public int getMoney_group() {
		return money_group;
	}

	public void setMoney_group(int money_group) {
		this.money_group = money_group;
	}

	public int getMoney_unit() {
		return money_unit;
	}

	public void setMoney_unit(int money_unit) {
		this.money_unit = money_unit;
	}
	
}
