package com.nmdy.operation.cityparam.dao;

public class LowSpeedOn {
	int id;
	String cityCode;
	int lowSpeedOn;

	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getCityCode() {
		return cityCode;
	}
	public void setCityCode(String cityCode) {
		this.cityCode = cityCode;
	}
	public int getLowSpeedOn() {
		return lowSpeedOn;
	}
	public void setLowSpeedOn(int lowSpeedOn) {
		this.lowSpeedOn = lowSpeedOn;
	}
}
